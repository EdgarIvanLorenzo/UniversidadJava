package EjerciciosPropios;

public class cortarCadena {
    public static void main(String[] args) {
        String cadena="Edgar Ivan";
        //Con el metodo substring de java cotamos una cadena
        System.out.println(cadena.substring(0,6));
        //Con el metodo charAt extraigo una letra dependiendo del indice
        System.out.println(cadena.charAt(0));
    }
}
