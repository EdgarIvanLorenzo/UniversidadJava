package Ternario;

public class operadorTernario {
    public static void main(String[] args) {
        //El operador ternario sirve para realizar una comparacion y obtener dos posibles resultados
        String resultado= (11%2==0)? "El 12 es par" : "El 12 es impar";
        //Se imprime el valor del resultado el cual es una cadena
        System.out.println(resultado);
    }
}
