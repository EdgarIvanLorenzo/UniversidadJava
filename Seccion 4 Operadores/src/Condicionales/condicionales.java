package Condicionales;

public class condicionales {
    public static void main(String[] args) {
        /*
        Los operadores condicionales son tres
        && and
        || or
        ! not
        Evaluan valores booleanos true y false
        * */
        //Operador And
        System.out.println((true && true));
        System.out.println((true && false));
        //Operador Or
        System.out.println((false || true));
        System.out.println((false||false));
        //Operador not
        System.out.println((!false)); //contrario de false=true
        System.out.println(!true); //Loi contrario de true=false
    }
}
